pub struct Transaction {
    id: TransactionId,
    posted: DateTime<Utc>,
    entered: DateTime<Utc>,
    action: Option<Action>,
    reference: Option<String>,
    description: Option<String>,
    splits: Vec<Split>,
    signature: Option<Signature>,
}

